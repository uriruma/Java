    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed
        try {
            deleteBook(bookSelected, authorSelected);
            JOptionPane.showMessageDialog(this, "Book deleted with exit!", "OK", JOptionPane.INFORMATION_MESSAGE);
            dispose();//this will close the window
        } catch (BookException e) {//It shouldn't be done but just in case
            JOptionPane.showMessageDialog(this, e.getMessage(), "Unable to delete the author", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_deleteButtonActionPerformed

